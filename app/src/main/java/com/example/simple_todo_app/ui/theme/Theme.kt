/**
 * Main application theme wrapper.
 * Handles Light/Dark mode, Dynamic Color on Android 12+, and system bar styling.
 */
@Composable
fun SimpleToDoAppTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    // Dynamic color is available on Android 12+ (API 31)
    dynamicColor: Boolean = true,
    content: @Composable () -> Unit
) {
    // Determine the color scheme based on user preference and system version
    val colorScheme = when {
        // Handle Dynamic Color (Material You) on Android 12+
        (dynamicColor && Build.VERSION.SDK_INT >= Build.VERSION_CODES.S) -> {
            val context = LocalContext.current
            if (darkTheme) dynamicDarkColorScheme(context) else dynamicLightColorScheme(context)
        }
        // Fallback to custom palettes for older versions or if dynamic color is disabled
        darkTheme -> DarkColorScheme
        else -> LightColorScheme
    }

    // Optional: Synchronize system bar appearance with the theme
    val view = LocalView.current
    if (!view.isInEditMode) {
        SideEffect {
            val window = (view.context as Activity).window
            // Since we use enableEdgeToEdge(), we just need to ensure the icons are visible
            WindowCompat.getInsetsController(window, view).isAppearanceLightStatusBars = !darkTheme
        }
    }

    MaterialTheme(
        colorScheme = colorScheme,
        typography = Typography,
        content = content
    )
}
